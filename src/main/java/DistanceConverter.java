public class DistanceConverter {
    public int kms_to_meters(int kms){
        return kms*1000;
    }
    public int meters_to_kms(int meters){
        return meters/1000;
    }
    public int cms_to_mms(int cms){
        return cms*10;
    }
    public int mms_to_cms(int mms){
        return mms/10;
    }
}
